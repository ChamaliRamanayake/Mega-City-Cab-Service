public class driver {
    
    private int driverID;
    private String driver_name;
    private String license_number;
    private int contact_number;
    private String availability;

    /**
     * @return the driverID
     */
    public int getDriverID() {
        return driverID;
    }

    /**
     * @param driverID the driverID to set
     */
    public void setDriverID(int driverID) {
        this.driverID = driverID;
    }

    /**
     * @return the driver_name
     */
    public String getDriver_name() {
        return driver_name;
    }

    /**
     * @param driver_name the driver_name to set
     */
    public void setDriver_name(String driver_name) {
        this.driver_name = driver_name;
    }

    /**
     * @return the license_number
     */
    public String getLicense_number() {
        return license_number;
    }

    /**
     * @param license_number the license_number to set
     */
    public void setLicense_number(String license_number) {
        this.license_number = license_number;
    }

    /**
     * @return the contact_number
     */
    public int getContact_number() {
        return contact_number;
    }

    /**
     * @param contact_number the contact_number to set
     */
    public void setContact_number(int contact_number) {
        this.contact_number = contact_number;
    }

    /**
     * @return the availability
     */
    public String getAvailability() {
        return availability;
    }

    /**
     * @param availability the availability to set
     */
    public void setAvailability(String availability) {
        this.availability = availability;
    }
    
}
